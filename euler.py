"""
#35: Circular primes

The number, 197, is called a circular prime because all rotations
of the digits: 197, 971, and 719, are themselves prime.

There are thirteen such primes below 100: 2, 3, 5, 7, 11, 13, 17, 31, 37, 71, 73, 79, and 97.

How many circular primes are there below one million?

Antwoord: 55
"""

"""
2, 3, 5, 7, 11, 13, 17, 31, 37, 71, 73, 79, 97, 113, 131, 197, 199, 311,
337, 373, 719, 733, 919, 971, 991, 1193, 1931, 3119, 3779, 7793, 7937, 9311,
9377, 11939, 19391, 19937, 37199, 39119, 71993, 91193, 93719, 93911, 99371,
193939, 199933, 319993, 331999, 391939, 393919, 919393, 933199, 939193,
939391, 993319, 999331
"""

def opdracht35(xmax = 10**6-1):
    def iscircular(n, lst):
        def rotations(n):
            def decimals(n):
                i = 0
                while n > 10**i:
                    i += 1
                return i
            def rotate(n):
                length = decimals(n)
                n, digit = divmod(n, 10)
                n += digit * 10**(length - 1)
                return n
            ret = list()
            for i in range(0, decimals(n)):
                n = rotate(n)
                ret.append(n)
            return ret
        rts = rotations(n)
        for rotation in rts:
            if rotation not in lst:
                return False
        return True
    def sieve(limit):
        a = [True] * limit
        a[0] = a[1] = False
        for i, isprime in enumerate(a):
            if isprime:
                yield i
                for n in range(i * i, limit, i):
                    a[n] = False
    primes = set(sieve(xmax))
    cprimes = set()
    for prime in primes:
        if iscircular(prime, primes):
            cprimes.add(prime)
    return len(cprimes)

