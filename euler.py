"""
#37: Truncatable primes

The number 3797 has an interesting property. Being prime itself, it is possible
to continuously remove digits from left to right, and remain prime at each
stage: 3797, 797, 97, and 7. Similarly we can work from right to left: 3797, 379, 37, and 3.

Find the sum of the only eleven primes that are both
truncatable from left to right and right to left.

NOTE: 2, 3, 5, and 7 are not considered to be truncatable primes.

Antwoord: 748,317
"""

"""
23 + 37 + 53 + 73 + 313 + 317 + 373 + 797 + 3,137 + 3,797 + 739,397 = 748,317
"""

def opdracht37():
    def isrighttruncatable(prime, primes):
        while prime > 10:
            prime = prime // 10
            if prime not in primes:
                return False
        return True
    def islefttruncatable(prime, primes):
        def decimals(n):
            i = 0
            while n > 10**i:
                i += 1
            return i
        def truncate_left(n):
            exp = decimals(n) - 1
            return n % 10**exp
        length = decimals(prime)
        for i in range(0, length):
            if prime not in primes:
                return False
            prime = truncate_left(prime)
        return True
    def sieve(limit):
        a = [True] * limit
        a[0] = a[1] = False
        for i, isprime in enumerate(a):
            if isprime:
                yield i
                for n in range(i * i, limit, i):
                    a[n] = False
    primes = set(sieve(999999))
    xsum = 0
    for prime in primes:
        if prime in [2,3,5,7]:
            continue
        if islefttruncatable(prime, primes) and isrighttruncatable(prime, primes):
            xsum += prime
    return xsum

