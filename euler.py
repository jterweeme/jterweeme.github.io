def opdracht42(words = words42):
    ret = 0
    triangles = set()
    def triangler(n):
        return n * (n + 1) >> 1
    for i in range(0, 20):      # 20 triangles ought to be enough
        triangles.add(triangler(i))
    def wordcount(word):
        count = 0
        for letter in word:
            count += ord(letter) - ord('A')
            count += 1
        return count
    for w in words:
        if wordcount(w) in triangles:
            ret += 1
    return ret

